def foo(s: str):
    """
    Функция, которая принимает на вход
    строку и возвращает ее со всеми
    заглавными буквами.
    :param s:
    :return:
    """
    return s.upper()


def foo2(s: str):
    """
    Функция, которая делает заглавными
    первые буквы каждого слова в строке.
    :param s:
    :return:
    """
    return s.title()
